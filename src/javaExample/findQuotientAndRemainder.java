package javaExample;

public class findQuotientAndRemainder
{
	public static void main(String[] args)
	{
		int a=5;
		int b=2;
		int quotient = a/b;
		int remainder = a%b;
		System.out.println("The quotient of "+a+" and "+b+" is : "+quotient);
		System.out.println("The remainder of "+a+" and "+b+" is : "+remainder);
	}
}
