package javaExample;

public class findASCII
{
	public static void main(String[] args)
	{
		char ch = 'a';
		int asciiValue = ch;
		char ch2 = 'z';
		int asciiValue2 = ch2;
		char ch3 = 'A';
		int asciiValue3 = ch3;
		char ch4 = 'Z';
		int asciiValue4 = ch4;
		System.out.println("ASCII value of "+ch+" is : "+asciiValue);
		System.out.println("ASCII value of "+ch2+" is : "+asciiValue2);
		System.out.println("ASCII value of "+ch3+" is : "+asciiValue3);
		System.out.println("ASCII value of "+ch4+" is : "+asciiValue4);
	}
}
